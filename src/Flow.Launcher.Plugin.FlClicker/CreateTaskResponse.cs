/// <summary>
/// Represents the response from a ClickUp task creation request.
/// </summary>
public class CreateTaskResponse {
  /// <summary>
  /// Gets or sets the task ID.
  /// </summary>
  public string Id { get; set; }

  /// <summary>
  /// Gets or sets the task name.
  /// </summary>
  public string Name { get; set; }
}
